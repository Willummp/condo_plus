package com.condoplus.condominio.estrutura.domain;

/**
 * Enumeração representando as tipologias de unidades habitacionais cobertas pelo condomínio.
 */
public enum TipoUnidade {

    /**
     * Unidade residencial localizada em edifício vertical (bloco de apartamentos).
     */
    APARTAMENTO,

    /**
     * Unidade residencial autônoma localizada em condomínio horizontal.
     */
    CASA
}
